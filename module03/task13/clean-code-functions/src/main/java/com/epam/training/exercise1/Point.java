package com.epam.training.exercise1;

/**
 * Point.
 *
 * @author Valeriia Biruk
 * @version 1.0
 */
public class Point {

    private final int x;
    private final int y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
